#include "libft.h"

void	*ft_memcpy(void *dst, void const *src, size_t n)
{
	unsigned char *p1;
	unsigned char *p2;

	p1 = (unsigned char *)((unsigned long)dst);
	p2 = (unsigned char *)((unsigned long)src);
	while (n--)
		*p1++ = *p2++;
	return (dst);
}
