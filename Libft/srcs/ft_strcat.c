#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	char *p;

	p = s1;
	while (*s1)
		s1++;
	while (*s2)
		*s1++ = *s2++;
	*s1 = '\0';
	return (p);
}
