#include "libft.h"

char	*ft_strncpy(char *dst, char const *src, size_t len)
{
	char *p;

	p = dst;
	while (len && *src)
	{
		*p++ = *src++;
		len--;
	}
	if (!*src)
		while (len-- && *p)
			*p++ = '\0';
	return (dst);
}
