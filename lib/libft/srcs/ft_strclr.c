#include "libft.h"

void	ft_strclr(char *s)
{
	size_t		i;
	size_t		j;

	i = 0;
	if (s == NULL)
		return ;
	j = ft_strlen(s);
	while (i < j)
	{
		s[i] = '\0';
		i++;
	}
}
