#include "libpt.h"

void	ft_putcrd(t_point crd)
{
	ft_putstr("x = ");
	ft_putnbr(crd.x);
	ft_putstr("  y = ");
	ft_putnbr(crd.y);
	ft_putchar('\n');
}
