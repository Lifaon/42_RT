#include "clust.h"

int		buf_to_int(char *buf)
{
	int				i;
	int				n;

	n = 0;
	i = -1;
	while (++i < 4)
	{
		n <<= 8;
		n |= buf[i] & 0xFF;
	}
	return (n);
}
